typedef struct {
    char ch;
    uint8_t fg_r, fg_g, fg_b;
    uint8_t bg_r, bg_g, bg_b;
} term_cell_t;

static term_cell_t term_buffer[TERM_ROWS][TERM_COLS];
static int cursor_col = 0;
static int cursor_row = 0;

static int pty_master = -1;
static pid_t shell_pid = -1;

static uint64_t get_time_ms(void) {
    struct timespec ts;
    clock_gettime(CLOCK_MONOTONIC, &ts);
    return (uint64_t)ts.tv_sec * 1000 + ts.tv_nsec / 1000000;
}

static void term_clear(void) {
    memset(term_buffer, 0, sizeof(term_buffer));
    for (int r = 0; r < TERM_ROWS; r++) {
        for (int c = 0; c < TERM_COLS; c++) {
            term_buffer[r][c].ch = ' ';
            term_buffer[r][c].fg_r = 0xCC;
            term_buffer[r][c].fg_g = 0xCC;
            term_buffer[r][c].fg_b = 0xCC;
            term_buffer[r][c].bg_r = 0x1e;
            term_buffer[r][c].bg_g = 0x1e;
            term_buffer[r][c].bg_b = 0x2e;
        }
    }
}

static void term_putchar(int col, int row, char ch) {
    if (col >= 0 && col < TERM_COLS && row >= 0 && row < TERM_ROWS) {
        term_buffer[row][col].ch = ch;
    }
}

static void term_scroll(void) {
    for (int r = 1; r < TERM_ROWS; r++) {
        memcpy(term_buffer[r-1], term_buffer[r], sizeof(term_buffer[0]));
    }
    for (int c = 0; c < TERM_COLS; c++) {
        term_buffer[TERM_ROWS-1][c].ch = ' ';
        term_buffer[TERM_ROWS-1][c].fg_r = 0xCC;
        term_buffer[TERM_ROWS-1][c].fg_g = 0xCC;
        term_buffer[TERM_ROWS-1][c].fg_b = 0xCC;
    }
}

