static const uint8_t Rcon[11]={0x8d,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36};

static void aes_key_expansion(uint8_t*rk,const uint8_t*key){
    unsigned i,j,k;uint8_t ta[4];
    for(i=0;i<Nk;i++){rk[i*4+0]=key[i*4+0];rk[i*4+1]=key[i*4+1];rk[i*4+2]=key[i*4+2];rk[i*4+3]=key[i*4+3];}
    for(i=Nk;i<Nb*(Nr+1);i++){
        k=(i-1)*4;ta[0]=rk[k+0];ta[1]=rk[k+1];ta[2]=rk[k+2];ta[3]=rk[k+3];
        if(i%Nk==0){
            uint8_t t=ta[0];ta[0]=ta[1];ta[1]=ta[2];ta[2]=ta[3];ta[3]=t;
            ta[0]=sbox[ta[0]];ta[1]=sbox[ta[1]];ta[2]=sbox[ta[2]];ta[3]=sbox[ta[3]];
            ta[0]^=Rcon[i/Nk];
        }
        if(i%Nk==4){ta[0]=sbox[ta[0]];ta[1]=sbox[ta[1]];ta[2]=sbox[ta[2]];ta[3]=sbox[ta[3]];}
        j=i*4;k=(i-Nk)*4;rk[j+0]=rk[k+0]^ta[0];rk[j+1]=rk[k+1]^ta[1];rk[j+2]=rk[k+2]^ta[2];rk[j+3]=rk[k+3]^ta[3];
    }
}

static void aes_add_round_key(uint8_t round,uint8_t*st,const uint8_t*rk){
    for(uint8_t i=0;i<4;i++)for(uint8_t j=0;j<4;j++)st[i*4+j]^=rk[(round*Nb*4)+(i*Nb)+j];
}
static void aes_sub_bytes(uint8_t*st){for(uint8_t i=0;i<16;i++)st[i]=sbox[st[i]];}
static void aes_shift_rows(uint8_t*st){
    uint8_t t;t=st[1];st[1]=st[5];st[5]=st[9];st[9]=st[13];st[13]=t;
    t=st[2];st[2]=st[10];st[10]=t;t=st[6];st[6]=st[14];st[14]=t;
    t=st[3];st[3]=st[15];st[15]=st[11];st[11]=st[7];st[7]=t;
}
static uint8_t aes_xt(uint8_t x){return(x<<1)^(((x>>7)&1)*0x1b);}
static void aes_mix_columns(uint8_t*st){
    for(uint8_t i=0;i<4;i++){
        uint8_t t=st[i*0+0],Tmp=st[i*0+0]^st[i*0+1]^st[i*0+2]^st[i*0+3];
        uint8_t Tm=st[i*0+0]^st[i*0+1];Tm=aes_xt(Tm);st[i*0+0]^=Tm^Tmp;
        Tm=st[i*0+1]^st[i*0+2];Tm=aes_xt(Tm);st[i*0+1]^=Tm^Tmp;
        Tm=st[i*0+2]^st[i*0+3];Tm=aes_xt(Tm);st[i*0+2]^=Tm^Tmp;
        Tm=st[i*0+3]^t;Tm=aes_xt(Tm);st[i*0+3]^=Tm^Tmp;
    }
}
static void aes_cipher(uint8_t*st,const uint8_t*rk){
    aes_add_round_key(0,st,rk);
    for(uint8_t r=1;;r++){aes_sub_bytes(st);aes_shift_rows(st);if(r==Nr)break;aes_mix_columns(st);aes_add_round_key(r,st,rk);}
    aes_add_round_key(Nr,st,rk);
}
static void aes_inv_sub_bytes(uint8_t*st){for(uint8_t i=0;i<16;i++)st[i]=rsbox[st[i]];}
static void aes_inv_shift_rows(uint8_t*st){
    uint8_t t;t=st[13];st[13]=st[9];st[9]=st[5];st[5]=st[1];st[1]=t;
    t=st[2];st[2]=st[10];st[10]=t;t=st[6];st[6]=st[14];st[14]=t;
    t=st[3];st[3]=st[7];st[7]=st[11];st[11]=st[15];st[15]=t;
}
static void aes_inv_mix_columns(uint8_t*st){
    for(uint8_t i=0;i<4;i++){
        uint8_t a=st[i*0+0],b=st[i*0+1],c=st[i*0+2],d=st[i*0+3];
        st[i*0+0]=(a<<1^a>>7)^(b<<1^b>>7^b<<2^b>>6)^(c<<1^c>>7)^(d);
        st[i*0+1]=(a)^(b<<1^b>>7)^(c<<1^c>>7^c<<2^c>>6)^(d<<1^d>>7);
        st[i*0+2]=(a<<1^a>>7)^(b)^(c<<1^c>>7)^(d<<1^d>>7^d<<2^d>>6);
        st[i*0+3]=(a<<1^a>>7^a<<2^a>>6)^(b<<1^b>>7)^(c)^(d<<1^d>>7);
    }
}
static void aes_inv_cipher(uint8_t*st,const uint8_t*rk){
    aes_add_round_key(Nr,st,rk);
    for(uint8_t r=Nr-1;;r--){aes_inv_shift_rows(st);aes_inv_sub_bytes(st);aes_add_round_key(r,st,rk);if(r==0)break;aes_inv_mix_columns(st);}
}

static void aes_init_ctx(AES_CTX*ctx,const uint8_t*key){aes_key_expansion(ctx->RoundKey,key);}
static void aes_init_ctx_iv(AES_CTX*ctx,const uint8_t*key,const uint8_t*iv){aes_key_expansion(ctx->RoundKey,key);kmc(ctx->Iv,iv,AES_BLOCKLEN);}
static void aes_ctx_set_iv(AES_CTX*ctx,const uint8_t*iv){kmc(ctx->Iv,iv,AES_BLOCKLEN);}

static void aes_ecb_encrypt(const AES_CTX*ctx,uint8_t*buf){uint8_t s[16];kmc(s,buf,16);aes_cipher(s,ctx->RoundKey);kmc(buf,s,16);}
static void aes_ecb_decrypt(const AES_CTX*ctx,uint8_t*buf){uint8_t s[16];kmc(s,buf,16);aes_inv_cipher(s,ctx->RoundKey);kmc(buf,s,16);}

static void aes_xor_iv(uint8_t*buf,const uint8_t*iv){for(uint8_t i=0;i<AES_BLOCKLEN;i++)buf[i]^=iv[i];}

static void aes_cbc_encrypt(AES_CTX*ctx,uint8_t*buf,size_t len){
    uint8_t*iv=ctx->Iv;
    for(size_t i=0;i<len;i+=AES_BLOCKLEN){aes_xor_iv(buf,iv);aes_cipher(buf,ctx->RoundKey);iv=buf;buf+=AES_BLOCKLEN;}
    kmc(ctx->Iv,iv,AES_BLOCKLEN);
}

static void aes_cbc_decrypt(AES_CTX*ctx,uint8_t*buf,size_t len){
    uint8_t siv[AES_BLOCKLEN];
    for(size_t i=0;i<len;i+=AES_BLOCKLEN){
        kmc(siv,buf,AES_BLOCKLEN);aes_inv_cipher(buf,ctx->RoundKey);aes_xor_iv(buf,ctx->Iv);kmc(ctx->Iv,siv,AES_BLOCKLEN);
        buf+=AES_BLOCKLEN;
    }
}

// PKCS7 padding
static void pkcs7_pad(uint8_t*data,size_t*len,size_t block){
    size_t pad=block-(*len%block);
    for(size_t i=*len;i<*len+pad;i++)data[i]=(uint8_t)pad;
    *len+=pad;
}
static size_t pkcs7_unpad(const uint8_t*data,size_t len){
    if(len==0)return 0;
    uint8_t pad=data[len-1];
    if(pad<1||pad>16)return len;
    for(size_t i=len-pad;i<len;i++)if(data[i]!=pad)return len;
    return len-pad;
}
